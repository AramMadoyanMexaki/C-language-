#include <stdio.h>

int main() {
    char d = 10;                 // Ստեղծում ենք մի փոփոխական `d` տիպով char, որի արժեքը սկզբում 10 է։
    char *gpt = &d;              // Ստեղծում ենք ցուցիչ `gpt`, որը պահում է փոփոխականի `d` հասցեն (հիշողության տեղը)։

    // Արտածում ենք տեքստ, որտեղ 
    // - gpt ցույց է տալիս, թե որ հասցեին է ցույց տալիս,
    // - *gpt ցույց է տալիս gpt հասցեում պահվող տվյալը (այսինքն `d`-ի արժեքը),
    // - d պարզապես ցույց է տալիս d-ի արժեքը։
    printf("gpt = %p, *gpt = %d, d = %d\n", gpt, *gpt, d);

    *gpt = 100;                  // gpt-ով մենք մտնում ենք `d`-ի հասցե և այնտեղ պահում նոր արժեք՝ 100։
    
    // Վերցնում ենք նույնը՝ նոր տպում ենք հասցեն, gpt-ով ցույց տված արժեքը, և d-ի արժեքը։ 
    // Հիմա d-ն արդեն 100 է, որովհետև gpt-ը ցույց էր տալիս d-ի հասցեին, և մենք փոխեցինք այդ հասցեում գտնվող արժեքը։
    printf("gpt = %p, *gpt = %d, d = %d\n", gpt, *gpt, d);

    return 0;                   // Ծրագրի ավարտ, վերադարձնում է 0։
}
